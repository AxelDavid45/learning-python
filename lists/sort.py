# We can sort a list alphabetically
my_list = ["car", "zebra", "company", "junior", "bear", "bird", "beer"]

print("Original list: " + str(my_list))

my_list.sort()  # We sorted the list
print("List sorted permanently in  alphabetical order: " + str(my_list))  # Print the new list

# In reverse alphabetical
my_list.sort()
